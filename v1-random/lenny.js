function filterable(elems) {
  return {
    all: elems,
    without: function(elemsToIgnore) {
      var filteredElems = [];
      for (var i in elems) {
        if (elemsToIgnore.indexOf(elems[i]) === -1) {
          filteredElems.push(elems[i]);
        }
      }
      return filteredElems;
    }
  };
}

